package br.com.aexo.atlas;

public class JoinedMarathonData {
	
	private String marathonApps;
	private String marathonTasks;

	public String getMarathonApps() {
		return marathonApps;
	}

	public void setMarathonApps(String marathonApps) {
		this.marathonApps = marathonApps;
	}

	public String getMarathonTasks() {
		return marathonTasks;
	}

	public void setMarathonTasks(String marathonTasks) {
		this.marathonTasks = marathonTasks;
	}

}